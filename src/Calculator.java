public class Calculator {
    public static double add(double num1, double num2){
//        "***add two number***"
        return num1+num2;
    }

    public static double subtract(double num1, double num2){
//         subtracting numbers
        return num1-num2;
    }
    
     public static double multiply(double n1, double n2){
//         multiplying numbers
        return n1*n2;
    }
}
