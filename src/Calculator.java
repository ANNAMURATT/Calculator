public class Calculator {
    public static double add(double num1, double num2){
//        "***add two number***"
        return num1+num2;
    }

    public static double subtract(double num1, double num2){
//         subtracting numbers
        return num1-num2;
    }

    public static double multiply(double num1, double num2){
//         multiplying numbers
        return num1*num2;
    }
}
